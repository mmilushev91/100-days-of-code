#Python Primitive Data Types

#1. Strings

"String" #indexes: 0 1 2 3 4 5

#Subscripting
print("String"[0]) # S

#challenge: print the last character of "Hello" String
print("Hello"[4]) # o
#negative indexes start counting from backwards
print("Hello"[-1]) # o
#everything inside double or single quotes is considered as String, even numbers
